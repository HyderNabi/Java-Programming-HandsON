
public class Arrays {
	public static void main(String args[])
	{
		//integer types ::::::::::
		int a = 34;
		short b = 3;
		byte c = 2;
		long d = 34553;
		System.out.println("a = "+a+" b = "+b+" c = "+c+" d = "+d);
		
		//floating types
		
		float e = 3;
		double f = 4.234324;
		System.out.println("e = "+e+" f = "+f);
		
		//Character types 
		
		char g = 'd';
		System.out.println("g = " + (int)++g);
		
		//
		String name = "HYDER NABI";
		System.out.println("The author is : " + name);
		
		
		
	}
}
 